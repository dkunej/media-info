		             <p>
                    The Scream (Norwegian: Skrik) is the popular name given to each of four versions of a composition, created as both paintings and pastels, by the Expressionist artist Edvard Munch between 1893 and 1910. Der Schrei der Natur (The Scream of Nature) is the title Munch gave to these works, all of which show a figure with an agonized expression against a landscape with a tumultuous orange sky. Arthur Lubow has described The Scream as "an icon of modern art, a Mona Lisa for our time."

Edvard Munch created the four versions in various media. The National Gallery, Oslo, holds one of two painted versions (1893, shown here). The Munch Museum holds the other painted version (1910, see gallery, below) and a pastel version from 1893. These three versions have not traveled for years.

The fourth version (pastel, 1895) was sold for $119,922,600 at Sotheby's Impressionist and Modern Art auction on 2 May 2012 to financier Leon Black, the third highest nominal price paid for a painting at auction. The painting was on display in the Museum of Modern Art in New York from October 2012 to April 2013.

Also in 1895, Munch created a lithograph stone of the image. Of the lithograph prints produced by Munch, several examples survive. Only approximately four dozen prints were made before the original stone was resurfaced by the printer in Munch's absence.

The Scream has been the target of several high-profile art thefts. In 1994, the version in the National Gallery was stolen. It was recovered several months later. In 2004, both The Scream and Madonna were stolen from the Munch Museum, and were both recovered two years later.</p>       
