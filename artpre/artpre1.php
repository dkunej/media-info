		             <p>
                     Gauguin travelled to Tahiti for the first time in 1891. His hope was to find "an edenic paradise where he could create pure, 'primitive' art",[5] rather than the primitivist faux works being turned out by painters in France. Upon arrival, he found that Tahiti was not as he imagined it: it had been colonised in the 18th century, and at least two-thirds of the indigenous people of the island had been killed by diseases brought by Europeans. "Primitive" culture had been wiped out. Despite this, he painted many pictures of native women: nude, dressed in traditional Tahitian clothes, and dressed in Western-style dresses, as is the rear figure in When Will You Marry?.


Full-scale working study, charcoal and pastel, Art Institute of Chicago
The front and middle ground are built up in areas of green, yellow and blue. A traditionally dressed young woman has settled on the threshold between the front and middle ground. Richard Field suggests the white tiare flower behind her left ear indicates she is seeking a husband. Behind her a second figure in a high-necked Western-style dress sits erectly. Field thought her gesture derives from Buddhist art.Naomi E. Maurer subsequently identified it as a mudra denoting threatening or warning. The front woman stretches herself, her facial features stylized and simplified. Field thought her pose had a Japanese precedent, Charles F. Stuckey suggests Delacroix's Women of Algiers. The rear female figure is flush with the yellow-blue area. Her face is painted with individual features and represents the centre of the image. The pink colour of her dress is clearly distinct from the other colours. At the bottom right is the inscription "NAFEA Faa ipoipo" (When will you marry). Gauguin commonly inscribed his paintings in Tahitian at this time: he was fascinated by the language, though never advanced beyond its rudiments.

Art historian Nancy Mowll Mathews wrote that Gauguin "portrayed the [Tahitian] natives as living only to sing and to make love. That's how he got the money from his friends and raised the public's interest in his adventure. But, of course, he knew the truth, which was that Tahiti was an unremarkable island with an international, westernised community". These paintings of Tahitians, including When Will You Marry?, were met with relative indifference when Gauguin returned to France, his 1893 Durand-Ruel exhibition only a limited success generating some favourable reviews but little by way of sales. Gauguin placed this painting on consignment at the exhibition at a price of 1,500 francs, the highest price he assigned and shared by only one other painting, but had no takers. Staechelin eventually purchased it at the Maison Moos gallery in Geneva in 1917.</p>       
