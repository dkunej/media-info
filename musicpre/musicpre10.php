                     Ray Charles was 27 years old in 1958, with ten years of experience recording primarily rhythm and blues music for the Downbeat and Swingtime record labels, in a style similar to that of Nat King Cole and Charles Brown. Charles signed with Atlantic Records in 1954 where producers Ahmet Ertegun and Jerry Wexler encouraged him to broaden his repertoire. Wexler would later remember that Atlantic Records' success came not from the artists' experience, but the enthusiasm for the music: "We didn't know shit about making records, but we were having fun". Ertegun and Wexler found that a hands-off approach was the best way of encouraging Charles. Wexler later said, "I realized the best thing I could do with Ray was leave him alone".

From 1954 into the 1960s Charles toured for 300 days a year with a seven-piece orchestra. He employed another Atlantic singing trio named the Cookies and renamed them the Raelettes when they backed him up on the road.[1] In 1954 Charles began merging gospel sounds and instruments with lyrics that addressed more secular issues. His first attempt was in the song "I Got a Woman", based either on the melodies of gospel standards "My Jesus Is All the World to Me" or an uptempo "I Got a Savior (Way Across Jordan)". It was the first Ray Charles record that got attention from white audiences, but it made some black audiences uncomfortable with its black gospel derivatives; Charles later stated that the joining of gospel and R&B was not a conscious decision.

In December 1958, he had a hit on the R&B charts with "Night Time Is the Right Time", an ode to carnality that was sung between Charles and one of the Raelettes, Margie Hendricks, with whom Charles was having an affair. Since 1956 Charles had also included a Wurlitzer electric piano on tour because he did not trust the tuning and quality of the pianos provided him at every venue. On the occasions he would play it, he was derided by other musicians.[4]</p>       
