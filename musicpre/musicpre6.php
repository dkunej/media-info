                    "Good Vibrations" is a song by American rock band the Beach Boys, released as a single in October 1966. The song was composed and produced by Brian Wilson with lyrics by Mike Love. Initiated during the sessions for the Pet Sounds album, it was not taken from or issued as a lead single for an album, but rather as a stand-alone single, with the Pet Sounds instrumental "Let's Go Away For Awhile" as a B-side. "Good Vibrations" would later be considered for the aborted Smile project, and ultimately was placed on the album Smiley Smile 11 months after its release.

Wilson has recounted that the genesis of the title "Good Vibrations" came from when his mother explained to him as a child that dogs sometimes bark at people in response to their bad vibrations. Fascinated by the concept, Wilson turned it into the general idea of limbic resonance or extrasensory perception, and developed the rest of the song as it was recorded.

Building upon the layered production approach he had previously formulated on Pet Sounds, Wilson recorded it piecemeal using several Los Angeles studios throughout the course of eight months, resulting in a cut-up mosaic of musical episodes marked by several discordant key and modal shifts which underlay choral fugues. Band publicist Derek Taylor dubbed the work a "pocket symphony," as it features an exotic array of instruments considered unusual for a popular song of its time, including prominent use of a jaw harp and the relatively new Electro-Theremin, along with conventional instruments played in ways novel to a pop hit, such as its cello and string bass which play a bowed tremolo over the song's chorus. The total production costs exceeded those of any music single ever produced.

Acknowledged as a work of 1960s modernism, Wilson is credited with further developing the use of the recording studio as an instrument. Its success earned the Beach Boys a Grammy nomination for Best Vocal Group performance in 1966 and the song was eventually inducted into the Grammy Hall of Fame in 1994.[10] An early psychedelic pop classic of the counterculture era, it has featured highly in many charts, being voted number one in the Mojo Top 100 Records of All Time chart in 1997 and number six on Rolling Stone‍ '​s list of the "500 Greatest Songs of All Time."[8] The song "Good Vibrations" is part of the The Rock and Roll Hall of Fame's 500 Songs that Shaped Rock and Roll list</p>       
