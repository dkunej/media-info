                    Le Rêve was purchased for $7,000 in 1941 by Victor and Sally Ganz of New York City. This purchase began their 50-year collection of works by just five artists: Picasso, Jasper Johns, Robert Rauschenberg, Frank Stella, and Eva Hesse. After the Ganzes died (Victor in 1987 and Sally in 1997), their collection, including Le Rêve, was sold at Christie's auction house on November 11, 1997, as a means of settling their inheritance tax bill. Le Rêve sold for an unexpectedly high $48.4 million, at the time the fourth most expensive painting sold (tenth when taking inflation into account).[citation needed] The entire collection set a record for the sale of a private collection, bringing $206.5 million. The total amount paid by the Ganzes over their lifetime of collecting these pieces was around $2 million.

