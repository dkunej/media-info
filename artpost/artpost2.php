		             <p>
                  In December 1954, Picasso began to paint a series of free variations on Delacroix's The Women of Algiers in their Apartment (Les Femmes d'Alger). He began his first version six weeks after learning of the death of his lifelong friend and rival Henri Matisse — and so, for Picasso, the "oriental" subject of this series of paintings held strong associations with Matisse as well as with Delacroix. Matisse had been famous for his images of languid, voluptuous women known as odalisques — the French form of the Turkish word for women in a harem. "When Matisse died he left his odalisques to me as a legacy," joked Picasso. Many of Picasso's portrayals of Jacqueline circa 1955–56 represent her in this guise).

The consequences of Picasso's Femmes d'Alger series were far-reaching: "I thought so much about Les Femmes d'Alger that I bought La Californie," Picasso explained to his biographer Pierre Daix. La Californie is a Belle-Époque villa situated in the foothills of Cannes in the South of France, where Picasso spent his last decades. Picasso bought it in 1955, and it was here that he painted the Nude in a Rocking-Chair . The light-filled interiors, the views over the Mediterranean and the exotic garden evoked a feeling of spaciousness and ease which corresponded to Picasso's idea of the Orient.

The art historian and collector Douglas Cooper was perhaps the first to realise that the paintings done at La Californie marked a return to Picasso's peak form. In April 1956, he wrote to the curator Alfred Barr:

"I recently spent the day with Picasso and went through most of what he has done since last July. I have been greatly impressed...A whole series of interiors of La Californie deriving half from Delacroix half from Matisse — great emphasis on ornament, arabesque, simplification...In short, as you are planning to come to Europe, this is a word to tell you that you must see all this in the studio: it is to my mind much better than anything since 1946.</p>       
