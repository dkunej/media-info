		             <p>
                     Rothko's move to New York established him in a fertile artistic atmosphere. Modernist painters were having more shows in New York galleries all the time, and the city's museums were an invaluable resource to foster a budding artist's knowledge and skills. Among the important early influences on Rothko were the works of the German Expressionists, the surrealist art of Paul Klee, and the paintings of Georges Rouault. In 1928, Rothko exhibited works with a group of other young artists at the appropriately named Opportunity Gallery. His paintings included dark, moody, expressionist interiors, as well as urban scenes, and were generally well accepted among critics and peers. Despite modest success, Rothko still needed to supplement his income, and in 1929 he began giving classes in painting and clay sculpture at the Center Academy, where he remained as teacher until 1952. During this time, he met Adolph Gottlieb, who, along with Barnett Newman, Joseph Solman, Louis Schanker, and John Graham, was part of a group of young artists surrounding the painter Milton Avery, fifteen years Rothko's senior. According to Elaine de Kooning, it was Avery who "gave Rothko the idea that [the life of a professional artist] was a possibility.Avery's stylized nature paintings, utilizing a rich knowledge of form and color, would be a tremendous influence on Rothko. Soon, Rothko's paintings took on subject matter and color similar to Avery's, as seen in Bathers, or Beach Scene of 1933-1934.

Rothko, Gottlieb, Newman, Solman, Graham, and their mentor, Avery, spent considerable time together, vacationing at Lake George and Gloucester, Massachusetts, spending their day painting and their evenings discussing art. During a 1932 visit to Lake George, Rothko met Edith Sachar, a jewelry designer, whom he married that fall. The following summer, his first one-man show was held at the Portland Art Museum, consisting mostly of drawings and aquarelles; for this exhibition, Rothko took the very unusual step of displaying work done by his pre-adolescent students from the Center Academy alongside his own. His family was unable to understand Rothko's decision to be an artist, especially considering the dire economic situation of the Depression. Having suffered serious financial setbacks, the Rothkowitzes were mystified by Rothko's seeming indifference to financial necessity; they felt he was doing his mother a disservice by not finding a more lucrative and realistic career.</p>       
