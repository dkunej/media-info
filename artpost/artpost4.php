		             <p>
                    The painting has been changed by Pollock since it was originally created. During January 1949, it was being shown in a solo Pollock show at the Betty Parsons gallery. It was from here that Alfonso A. Ossorio decided to purchase a "paint drip" composition; he chose No.5, 1948 and paid $1,500. It was the only canvas sold from the show. At some point, presumably during the moving process, the painting became damaged according to Grace Hartigan. The shipping company "Home Sweet Home came in with a painting in one hand and a lump of paint from the center of the painting in the other hand". Hartigan gave Pollock some paint and he patched the painting before it went to Ossorio saying "He’ll never know, never know". When the painting was subsequently delivered to Ossorio, he claimed that he noticed "a portion of the paint - actually the skin from the top of an opened paint can - had slid" leaving a "nondescript smear amidst the surrounding linear clarity," as he explained in a 1978 lecture at Yale. Pollock offered to rework the painting but, according to Hartigan, he "repainted the whole thing again" and stated that "He'll never know. No one knows how to look at my paintings, he won’t know the difference." After three weeks, Ossorio visited Pollock’s studio to inspect the painting. Ossorio was confronted with an artwork which had "new qualities of richness and depth" as a result of Pollock’s "thorough but subtle overpainting." It was clear that Ossorio still liked the painting despite the rework and continued to attest that the "original concept remained unmistakably present, but affirmed and fulfilled by a new complexity and depth of linear interplay. It was, and still is a masterful display of control and disciplined vision." Pollock repaired the damage to the painting by completely altering the original, in contrast to how other artworks are repaired. The reconstruction had not only retained but reinforced the metaphysical concept of the painting and has become what Ossorio calls "a wonderful example of an artist having a second chance".</p>       
